let array  = [1,2,3,4,5];
printToScreen(array);

//push to last index
array.push(6)
printToScreen(array);

//remove from last index
printToScreen(array.pop());
printToScreen(array);


//insert to first index
printToScreen(array.unshift(0));
printToScreen(array);


//remove to first index
printToScreen(array.shift(0));
printToScreen(array);